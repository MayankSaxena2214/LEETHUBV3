class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int ans=0;
        string num=to_string(n);
        for(auto ch:num){
            int val=ch-'0';
            ans+=pow(val,3);
        }
        if(ans==n)return "true";
        return "false";
    }
};

