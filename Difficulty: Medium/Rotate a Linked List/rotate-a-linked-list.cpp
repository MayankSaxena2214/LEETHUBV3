class Solution {
  public:
    // Function to rotate a linked list.
    Node* rotate(Node* head, int k) {
        // Your code here
        //Make it circular first
        Node*temp=head;
        int len=0;
        while(temp->next!=NULL){
            temp=temp->next;
            len++;
        }
        temp->next=head;
        if(k>len){
            k=k%(len+1);
        }
        if(k==0 || head==NULL || head->next==NULL){
            temp->next=NULL;
            return head;
        }
        int moves=k;
        Node*curr=head,*temp1=curr;
        while(moves!=0){
            temp1=curr;
            curr=curr->next;
            moves--;
        }
        temp1->next=NULL;
        return curr;
        
    }
};


